function writeFile($filepath, $filename, $content){
	if(file_exists($filepath."/".$filename)){
		unlink($filepath."/".$filename);
	}
	if (!$handle = @fopen($filepath."/".$filename, 'w')) { 
		return false; 
	} 
	if (!fwrite($handle, $content)) { 
		return false; 
	} 
	fclose($handle); 
	return true; 
}

